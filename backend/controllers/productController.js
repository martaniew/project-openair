import asyncHandler from 'express-async-handler'
import Product from '../models/productModel.js'
import getCoordsForAddress from '../utils/location.js'

// @desc    Fetch all products
// @route   GET /api/products
// @access  Public
const getProducts = asyncHandler(async (req, res) => {
  const pageSize = 10
  const page = Number(req.query.pageNumber) || 1

  
  console.log("params")
  console.log(req.query.searchParams); 

  let searchedPhrases = JSON.parse(req.query.searchParams);
  console.log(searchedPhrases); 

  let keywords = {};

if (Object.keys(searchedPhrases).length === 0) {
  keywords={}
   }
  else { 
  for (let key in searchedPhrases) {
       let searchedPhrase = searchedPhrases[key]; 
       keywords[key] =  { $regex : searchedPhrase, $options: 'i'}   
  }
}
console.log(keywords); 


  

//console.log(keywords); 

  const count = await Product.countDocuments(keywords)
  console.log(count)
  const products = await Product.find(keywords)
    .limit(pageSize)
    .skip(pageSize * (page - 1))

  res.json({ products, page, pages: Math.ceil(count / pageSize) })
})

// @desc    Fetch single product
// @route   GET /api/products/:id
// @access  Public
const getProductById = asyncHandler(async (req, res) => {
  const product = await Product.findById(req.params.id)

  if (product) {
    res.json(product)
  } else {
    res.status(404)
    throw new Error('Product not found')
  }
})

// @desc    Delete a product
// @route   DELETE /api/products/:id
// @access  Private/Admin
const deleteProduct = asyncHandler(async (req, res) => {
  const product = await Product.findById(req.params.id)

  if (product) {
    await product.remove()
    res.json({ message: 'Product removed' })
  } else {
    res.status(404)
    throw new Error('Product not found')
  }
})

